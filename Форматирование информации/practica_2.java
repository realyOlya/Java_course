import java.io.PrintStream;

/**
 * Метод flush() принудительно записывает данные из буфера в выходной поток.
 * Некоторые выходные потоки (например, PrintStream, FileOutputStream) буферизируют данные,
 * чтобы повысить производительность. Данные копятся в буфере, и только при заполнении
 * буфера или закрытии потока происходит реальная запись.
 * 
 * flush() позволяет немедленно записать данные, не дожидаясь заполнения буфера.
 * Это важно, когда нужно гарантировать, что данные уже записаны (например, при логировании,
 * отправке сообщений по сети, записи важных данных).
 */
public class FlushMethodDemo {
    public static void main(String[] args) {
        // System.out – это PrintStream, который может буферизировать вывод
        System.out.print("Это сообщение может задержаться в буфере");
        System.out.flush(); // принудительно выводим на консоль
        
        // Пример без flush: если буфер не заполнен, данные могут не появиться сразу
        // (но в консоли обычно автопромывка происходит при переводе строки %n)
        
        PrintStream ps = System.out;
        ps.print("Текст без перевода строки и без flush – может не вывестись сразу");
        ps.flush(); // теперь точно выведется
        
        // Ещё пример: при записи в файл
        // FileOutputStream fos = new FileOutputStream("file.txt");
        // fos.write("data".getBytes());
        // fos.flush(); // гарантирует запись на диск
        // fos.close();
    }
}