public static void main(String[] args) {
    try {
        int result = 10 / 0; // возникнет ArithmeticException
    } finally {
        System.out.println("Это выполнится всегда — до выброса исключения выше");
    }
    // Исключение уйдёт в JVM и вызовет аварийное завершение
}
//Да, такая конструкция допустима.
//Она используется, когда нужно гарантированно выполнить некоторые действия (например, закрытие ресурсов), но само исключение не обрабатывается в текущем методе, а передаётся выше по стеку.
