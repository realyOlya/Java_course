public class FinalCatchDemo {

    public static void main(String[] args) {
        try {
            throw new NullPointerException("Тестовое исключение");
        } catch (final Exception e) {
            // Параметр e объявлен как final – его нельзя переназначить внутри блока catch
            System.out.println("Исключение: " + e.getMessage());

            // Следующая строка вызовет ошибку компиляции, если раскомментировать:
            // e = new RuntimeException("Новое исключение"); // нельзя изменить final переменную

            // Однако сам объект исключения (его поля) можно изменять (если они не final)
            // Например, e.initCause(...) – допустимо, т.к. ссылка final, а не содержимое объекта.
        }
    }
}
//catch (final Exception e) означает, что переменная e (ссылка на объект исключения) не может быть изменена внутри блока catch. То есть нельзя присвоить e другой объект. Это полезно для предотвращения случайной перезаписи ссылки, что делает код более надёжным и выразительным. Сам объект исключения при этом остаётся изменяемым (можно менять его состояние через методы типа setStackTrace() или initCause()).