class A {
    int a;
    int b;
    int c;
    int z;

    // Самый полный конструктор, который делает основную работу
    public A(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.z = 1;
    }

    // Конструктор с двумя параметрами вызывает полный
    public A(int a, int b) {
        this(a, b, 0); // Передаем c = 0 (или любое дефолтное значение)
    }

    // Конструктор с одним параметром вызывает версию с двумя
    public A(int a) {
        this(a, 0);
    }

    // Конструктор без параметров вызывает версию с одним
    public A() {
        this(0);
    }
}